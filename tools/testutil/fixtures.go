// findTestdataDir locates `tools/meetingdata/testdata/` relative to
// the module root. Reuses findModuleRoot from binary.go.
func findTestdataDir() (string, error) {
	moduleRoot, err := findModuleRoot()
	if err != nil {
		return "", err
	}
	dir := filepath.Join(moduleRoot, "meetingdata", "testdata")
	if _, err := os.Stat(dir); err != nil {
		return "", fmt.Errorf("findTestdataDir: %s: %w", dir, err)
	}
	return dir, nil
}
